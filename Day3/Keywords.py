'''
In Python, keywords are reserved words that have special meaning in the language. You cannot use them as variable names, function names, etc.

Python keywords can be grouped into several types/categories:


---

✅ 1. Control Flow Keywords

Used for decision-making and loops.

if, elif, else

for, while

break, continue

pass



---

✅ 2. Boolean & Logical Keywords

Used for logic operations.

True, False

and, or, not



---

✅ 3. Variable Definition & Deletion Keywords

Used for assigning or deleting objects.

None

del



---

✅ 4. Function & Class Definition Keywords

Used for building functions, classes, decorators.

def

class

return

yield (for generators)

lambda (for anonymous functions)



---

✅ 5. Exception Handling Keywords

Used to handle errors.

try

except

finally

raise

assert



---

✅ 6. Import Keywords

Used to include modules.

import

from

as



---

✅ 7. Context Management Keywords

Used for writing context managers.

with

async, await (for asynchronous programming)



---

✅ 8. Class-Related Keywords

Used inside classes.

global (declare global variable)

nonlocal (declare variable from enclosing scope)

in (used for membership testing)

is (identity testing)


'''
